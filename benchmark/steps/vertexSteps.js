export async function registerVertexSteps({ bench, g }) {
  const [a] = await g.addV('person')
  const [b] = await g.addV('person')
  const [c] = await g.addV('project')
  const [d] = await g.addV('person')
  await g.addE('knows', a, b)
  await g.addE('created', a, c)
  await g.addE('knows', d, a)
  const va = g.V(a)
  await va.property('name', 'alpha')
  await va.property('score', 42)
  await g.V(d).property('score', 7)
  const vertexSet = [a, b, c, d]

  bench.add('V.id()', async () => { await va.id() })
  bench.add('V.label()', async () => { await va.label() })
  bench.add('V.properties("name")', async () => { await va.properties('name') })
  bench.add('V.valueMap()', async () => { await va.valueMap() })
  bench.add('V.has(label)', async () => { await va.has('label', 'person') })
  bench.add('V.has(prop)', async () => { await va.has('name', 'alpha') })
  bench.add('V.property()', async () => { await va.property('bench', 1) })
  bench.add('V.outE()', async () => { await va.outE() })
  bench.add('V.out()', async () => { await va.out() })
  bench.add('V.inE()', async () => { await va.inE() })
  bench.add('V.in()', async () => { await va.in() })
  bench.add('V.bothE()', async () => { await va.bothE() })
  bench.add('V.drop()', async () => {
    const [vx] = await g.addV('tmp')
    await g.V(vx).drop()
  })
  bench.add('V.limit()', async () => { await g.V(vertexSet).limit(2) })
  bench.add('V.tail()', async () => { await g.V(vertexSet).tail(2) })
  bench.add('V.count()', async () => { await g.V(vertexSet).count() })
  bench.add('V().has(label)', async () => { await g.V().has('label', 'person') })
  bench.add('V.filter()', async () => { await g.V([a, b, d]).filter((__) => __.out()) })
  bench.add('V.and()', async () => { await g.V([a, b, d]).and((__) => __.out(), (__) => __.has('score', 42)) })
  bench.add('V.or()', async () => { await g.V([a, b, d]).or((__) => __.out(), (__) => __.has('score', 7)) })
  bench.add('V.not()', async () => { await g.V([a, b, d]).not((__) => __.out()) })
  bench.add('V.where()', async () => { await g.V(a).as('origin').out().where((__, { value, select }) => value !== select('origin')) })
  bench.add('V.path()', async () => { await g.V(a).out().path() })
  bench.add('as + select', async () => {
    await g.V(a).as('origin').out('knows').as('friend').select('origin', 'friend')
  })
  bench.add('values.limit()', async () => { await g.V(vertexSet).id().limit(2) })
  bench.add('values.tail()', async () => { await g.V(vertexSet).id().tail(1) })
  bench.add('values.filter()', async () => { await g.V(vertexSet).id().filter((id) => id === a || id === b) })
  bench.add('values.and()', async () => { await g.V(vertexSet).id().and((id) => id === a, (id) => id !== c) })
  bench.add('values.or()', async () => { await g.V(vertexSet).id().or((id) => id === a, (id) => id === d) })
  bench.add('values.not()', async () => { await g.V([a, b]).id().not((id) => id === a) })
  bench.add('values.where()', async () => { await g.V([a, b]).id().where((id) => id === a) })
  bench.add('values.count()', async () => { await g.V(vertexSet).id().count() })
}
